package com.ejercicio;

public class App2 {
    public static void main(String[] args) {
        //Estructuras Repetitivas
        int numero = 1;
        do {
            System.out.println("Numero" + numero);
            numero ++;
        } while (numero <= 6);

        for (int i=1; i <= 6; i++) {
            System.out.println("Numero" + i);
        }
    }
}
